        <p>
            “Follow-through” refers to a short moment after the main action. It is the idea that body parts of
            characters or elements of animated objects might continue to move even after a motion is completed. This
            effect is also called anti-anticipation.
        </p>
        <p>
            Preparation and recovery moments in action
            Every primary action (a jump, a bow, a twirl) has a moment of preparation and a moment of recovery. This
            moment necessarily should be a part of an animated video. “Follow through” makes the action more realistic
            and easy to believe.
        </p>
