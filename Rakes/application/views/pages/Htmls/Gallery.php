        <b>Bouncing Ball</b>
        <pre id="Mtext">
            The bouncing ball technique conveys realism by mimicking how a real ball behaves under the influence of gravity and momentum. 
            By showing how the ball accelerates on its way down and decelerates as it reaches its peak height, 
            animators can create a believable sense of weight.

            Bouncing a ball seems like a simple exercise to complete, however there are some fundamental aspects that
            must be outlined, such the key principles of animation required when animating. 
            When animating a ball we will be focusing on 6 of the 12 principles of animation, 
            these are: 
            
            Timing and Spacing, 
            
            Pose to pose/Straight ahead, 
            
            Ease in/Ease out, 
            
            Squash and stretch, 
            
            Solid form and Archs.
