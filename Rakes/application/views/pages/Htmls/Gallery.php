        <p>
            Anticipation is the preparation for an action such as a jump or a punch. For example, a character will squat
            down before pushing his body and legs up into a jump. The stronger the anticipation motion, the more
            cartoony and fluid the animation will be.
        </p>
        <p>
            Anticipation refers to a brief moment of action that precedes the main action of an animated scene. Almost
            all real movements contain preparation for it – squatting before jumping, crouching down before running,
            swinging before throwing a stone, etc. This moment necessarily should be a part of an animated video.
            Therefore, if animators add an anticipation drawing to their works, the audience will be prepared for the
            main action in the scene. This would help to make the action appear more realistic. Moreover, the audience
            can always see everything clearly, so that they can foresee what is about to happen.
        </p>
