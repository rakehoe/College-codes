        <p>
            Secondary action refers to the actions that support or emphasize the main action to breathe more life into
            the animation and create a more convincing performance.
        </p>
        <p>
            This is an additional action that supports the main act. A secondary action is an integral part of animation
            as it adds interest and realism to the scene. It originates from immediate action, such as when someone’s
            arm moves backward after throwing something forward.
            Secondary actions are helpful when animating something like a walk cycle where one leg will swing forward
            while the other steps backward, creating balance as you go along with your motion. Hence, not all weight
            falls onto just one side making things look less stiff than they would otherwise be without this addition.
        </p>
        <video src="../Vid/Secondary Action.mp4" type="video/mp4" controls loop></video>
