        <p class="title">Love is a Fallacy</p>
        <p class="author">By Max Shulman</p>
        <br>
        <br>
        <p class="story" id="start">
            Cool was I and logical. Keen, calculating, perspicacious, acute and astute—I was all of these. My brain was
            as
            powerful as a dynamo, precise as a chemist’s scales, as penetrating as a scalpel. And—think of it!—I am only
            eighteen.
            <br>
            <br>
        <p class="story" id="start">
            It is not often that one so young has such a giant intellect. Take for example, Petey Bellows, my roommate
            at
            the university. Same age, same background, but dumb as an ox. A nice enough fellow, you understand, but
            nothing
            upstairs. Emotional type. Unstable. Impressionable. Worst of all, a faddist. Fads, I submit, are the very
            negation of reason. To be swept up in every new craze that comes along, to surrender oneself to idiocy just
            because everybody else is doing it—this, to me, is the acme of mindlessness. Not, however, to Petey.
            <br>
            <br>
        <p class="story" id="start">
            One afternoon I found Petey lying on his bed with an expression of such distress on his face that I
            immediately
            diagnosed appendicitis. “Don’t move,” I said, “Don’t take a laxative. I’ll get a doctor.”</p>
