                    The bouncing ball technique conveys realism by mimicking
                    how a real ball behaves under the influence of gravity and momentum.
                    By showing how the ball accelerates on its way down
                    and decelerates as it reaches its peak height,
                    animators can create a believable sense of weight.
                </p>
