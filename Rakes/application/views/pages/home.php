                    Bouncing a ball seems like a simple exercise to complete,
                    however there are some fundamental aspects that
                    must be outlined, such the key principles of animation
                    required when animating.
                    When animating a ball we will be focusing on
                    6 of the 12 principles of animation, these are:

                    <li id="Mtext" style="text-indent: 20%;">Timing and Spacing, </li>
                    <li id="Mtext" style="text-indent: 20%;">Pose to pose/Straight ahead, </li>
                    <li id="Mtext" style="text-indent: 20%;">Ease in/Ease out, </li>
                    <li id="Mtext" style="text-indent: 20%;">Squash and stretch, </li>
                    <li id="Mtext" style="text-indent: 20%;">Solid form and Archs.</li>
                </p>
