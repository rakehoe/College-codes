            <div id="Anticipation" class="sub-container">
                <b id="Text-Title">Anticipation</b>
                <p id="Mtext">
                    Anticipation is the preparation for an action such as a jump or a punch.

                    For example, a character will squat
                    down before pushing his body and legs up into a jump.
                    The stronger the anticipation motion, the more
                    cartoony and fluid the animation will be.
                </p><br>
                <p id="Mtext">
                    Anticipation refers to a brief moment of action that precedes
                    the main action of an animated scene.
                    Almost all real movements contain preparation for it –
                    squatting before jumping, crouching down before running,
                    swinging before throwing a stone, etc.
                </p><br>
                <p id="Mtext">
                    This moment necessarily should be a part of an animated video.
                    Therefore, if animators add an anticipation drawing to their works,
                    the audience will be prepared for the main action in the scene.
                </p><br>
                <p id="Mtext">
                    This would help to make the action appear more realistic.
                    Moreover, the audience can always see everything clearly,
                    so that they can foresee what is about to happen.
                </p>
            </div>
            <div id="SecondaryAction" class="sub-container">
                <b id="Text-Title">Secondary Action</b>
                <p id="Mtext">
                    Secondary action refers to the actions that support or
                    emphasize the main action to breathe more life into the
                    animation and create a more convincing performance.
                </p><br>
                <p id="Mtext">

                    This is an additional action that supports the main act.
                    A secondary action is an integral part of animation
                    as it adds interest and realism to the scene.
                    It originates from immediate action, such as when someone’s
                    arm moves backward after throwing something forward.
                </p><br>
                <p id="Mtext">

                    Secondary actions are helpful when animating something
                    like a walk cycle where one leg will swing forward while the
                    other steps backward, creating balance as you go along with your motion.
                </p><br>
                <p id="Mtext">

                    Hence, not all weight falls onto just one side making
                    things look less stiff than they would otherwise be without this addition.
                </p>
            </div>
            <div id="FollowThrough" class="sub-container">
                <b id="Text-Title">Follow Through</b>
                <p id="Mtext">
                    “Follow-through” refers to a short moment after the main action.

                    It is the idea that body parts of characters or elements of
                    animated objects might continue to move even after a motion is completed.
                </p><br>
                <p id="Mtext">
                    This effect is also called anti-anticipation.
                    Preparation and recovery moments in action.
                    Every primary action (a jump, a bow, a twirl)
                    has a moment of preparation and a moment of recovery.
                </p><br>
                <p id="Mtext">
                    This moment necessarily should be a part of an animated video.
                    “Follow through” makes the action more realistic and easy to believe.
                </p>
            </div>
            <div id="WalkCycle" class="sub-container">
                <b id="Text-Title">Walk Cycle</b>
                <p id="Mtext">
                    A walk cycle is an animation technique using
                    illustrated frames stitched together to create a sense of movement.
                    The series of frames contains consecutive drawings of a walking figure.
                </p><br>
                <p id="Mtext">
                    When combined together, the animation appears to walk.
                    In animation, a walk cycle is a series of frames or
                    illustrations drawn in sequence that loop to create
                    an animation of a walking character.
                </p><br>
                <p id="Mtext">
                    The walk cycle is looped over and over,
                    thus avoiding having to animate each step again.
                </p>
            </div>
            <div id="Arc" class="sub-container">
                <b id="Text-Title">Arc</b>
                <p id="Mtext">
        Arc is the movement in circular paths. 
    Human beings do not move in one straight direction. 
    They move in arcs. In the same way, the arc technique 
    is to help animation create that illusion of movement, 
    for example, the bouncing of the ball, 
    as shown in the illustration in the right.
        </p><br>
        <p id="Mtext">
        The movements and actions that we see in nature 
    and living beings in our day to day lives are 
    very rarely mechanical in and out or up and down motions.
    Generally, most living creatures follow an implied curved path, 
    also known as an “arc”.
    </p><br>
    <p id="Mtext">
        Meaning to say that all the actions that we 
    generally see in our day to day lives follow arcs.
                </p>
